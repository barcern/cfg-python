# Write a Python program to sum all the items in a list.
# e.g. for the list nums = [10,32,12,11,9] we should get an output of 74

# Create a list
numbers = [3, 12, 65, 23]

# Initiate sum variable
total_sum = 0

# Sum all numbers in the list
for i in numbers:
    total_sum += i

# Print the total sum
print(total_sum)
